    private void logoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutButtonActionPerformed
        //will dispose this form and open LoginForm.java if user clicks Yes button
        int ans = JOptionPane.showConfirmDialog(this, "Are You Sure?", "Logout?", JOptionPane.YES_NO_OPTION);
        if (ans == 0) {
            java.awt.Window win[] = java.awt.Window.getWindows();
            for (int i = 0; i < win.length; i++) {
                win[i].dispose();
            }
            LoginForm loginForm = new LoginForm();
            loginForm.setVisible(true);
            LoginForm.shopName.setSelectedItem(shopName1);

            this.dispose();
        }
    }//GEN-LAST:event_logoutButtonActionPerformed

