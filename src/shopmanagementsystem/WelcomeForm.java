    public WelcomeForm() {
        initComponents();

        //Full Screen
        Toolkit tk = Toolkit.getDefaultToolkit();
        int xsize = (int) tk.getScreenSize().getWidth();
        int ysize = (int) tk.getScreenSize().getHeight();
        this.setSize(xsize, ysize);
        this.setExtendedState(this.getExtendedState() | JFrame.MAXIMIZED_BOTH);

        //Getting shop name & first name from LoginForm.java & database
        String id1 = LoginForm.id.getText();
        shopName1 = LoginForm.shopName.getSelectedItem().toString();
        try {
            c = DB.java_db();
            s = c.createStatement();
            rs = s.executeQuery("SELECT * FROM shop WHERE id=" + id1);
            String firstName1 = "", lastName1 = "";
            while (rs.next()) {
                firstName1 = rs.getString("firstName");
                lastName1 = rs.getString("lastName");
            }
            welcomeMessage.setText("Welcome " + firstName1 + " " + lastName1 + "! Connected To Shop : " + shopName1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(WelcomeForm.this, e);
        } finally {
            try {
                rs.close();
                s.close();
                c.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(WelcomeForm.this, e);
            }
        }

        //Setting up image
        Image image = new ImageIcon(this.getClass().getResource("Images/image1.png")).getImage();
        Image newImage = image.getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon newImageIcon = new ImageIcon(newImage);
        imageLabel.setIcon(newImageIcon);

    }

